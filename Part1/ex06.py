# List of names
names = ['Tom', 'Jerry', 'Butch', 'Tuffy', 'Pecos']

# Print the whole list
print(names)  # ['Tom', 'Jerry', 'Butch', 'Tuffy', 'Pecos']
# Print the first name
print(names[0])  # 'Tom'
# Print the last name
print(names[-1])  # 'Pecos'
# Print the middle two names using list slicing
print(names[2:4])  # ['Butch', 'Tuffy']